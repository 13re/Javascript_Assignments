// Variables 

// Variables are useful when adjusting multiple parameters of the same number.
// Saves time when changing the size of multiple shapes. 

// Method_1_of_assigning_variables___
var dot; 
dot = 80;  // dot gets 80. Dot is now assigned the value 80. 

// Method_2_of_assigning_variables___
var square = 25; // square gets 25. 

// to change the size of the squares & dots,
// adjust the numbers on the variables above once
// (rather than adjusting multiple times below)

ellipse(50, 50, dot, dot); 
ellipse(100, 100, dot, dot); 
rect(50, 100, square, square); 
rect(100, 50, square, square);

